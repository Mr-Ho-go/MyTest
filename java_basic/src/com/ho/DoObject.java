/**
 * sout方法探索
 */
public class DoObject {

    public static void main(String[] args) {



        char[] ch = new char[]{'1','2','3'};

        // 对于char数组，打印的是数值
        System.out.println(ch);

        int[] ints = new int[]{1,45,67,88,33};

        // 对于int数组，打印的时地址值
        System.out.println(ints);




    }
}
